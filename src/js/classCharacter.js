// Class Character
class Character {
	constructor(x, y, w, h) {
		this.x = x;
		this.y = y;
		this.w = w;
		this.h = h;
	}
	
	getX() {
		return this.x;
	}

	setX(x) {
		this.x = x;
	}

	getY() {
		return this.y;
	}

	setY(y) {
		this.y = y;
	}

	getW() {
		return this.w;
	}

	setW(w) {
		this.w = w;
	}

	getH() {
		return this.h;
	}

	setH(h) {
		this.h = h;
	}
}