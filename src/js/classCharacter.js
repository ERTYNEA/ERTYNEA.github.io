// Class Character
class Character {
    constructor(name, type, speed) {
        this.name = name;
        this.type = type;
        this.speed = speed;
    }

    getName() {
        return this.name;
    }

    setName(name) {
        this.name = name;
    }

    getType() {
        return this.type;
    }

    setType(type) {
        this.type = type;
    }

    getSpeed() {
        return this.speed;
    }

    setSpeed(speed) {
        this.speed = speed;
    }
}
